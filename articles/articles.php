        <h2>How to program?</h2>
		<h4>-By Utkarsh Garg</h4>
        <p>
        Well this is a question that many people ask but only a few know the answer to – How to program? Some say programmers are born while others simply think that it is too much for them to do. Well my friends that is not the case. I hope after you read this article you will start thinking differently.
        </p>
        <p>
        I have seen people trying to learn programming and fail many a times. I too was once in school and like most of my friends I never understood what programming actually was. To me it was gibberish. At one point I remember failing in Computer Science for my pre-board in class 10th. My teacher then taunted me – "You will never be able to code!" It was a big blow for me – for the first time in my life I had flunked an exam and that too just before my 10th boards. And you know how parents get when their children fail exams – especially the pre-board examinations. So I decided that I will try my best and try and understand what exactly did this textbook on "Java" want from me. I started off – studying chapter after chapter, line by line, trying to understand everything that was there in the book. The more I read the easier it seemed. I realized that programming is nothing but means through which I could make my computer do whatever I wanted. And I could do it by using the resources that the programming language provided me with – and believe me there are quite a number of resources that programming languages offer these days. All I needed to do was use the right thing in the right place. And hence began my journey and today thanks to God and my family's support I am doing my B.Tech in CSE! [Tell that to my Computer teacher in class 10!]
        </p>
        <p>
        So the question is – how should you start? Well anytime is the right time to start. You can start right now if you want! You see it is very simple. Buy a book and start reading or go online and enrol yourself in some online course for programming for starters. Select any programming language that you feel like studying and start right away. But remember – you need to have is lots of patience because programming is not just learning the syntax of any language from books or finishing of some online or offline course. It is essential to know them but the task is still half done. 
        </p>
        <p>
        You see the essence lies in the logic. If the program is a body then logic is like its soul. Logic is the means through which we actually develop algorithms and then write programs – a program is nothing if you cannot imagine how you are going to do it. It is lifeless and logic is the means by which we bring any program to life. It is the brain that you gave to your program to perform the task you want your computer to do. You may now ask me – "How in the world do I learn logic?" Well honestly logic isn't something that you can learn off a book or do a course on. Logic is something that one needs to develop.  And how do you do that? Well it's simple – just start thinking! Use your brains. It takes a lot of practice and hard work to do this but all of us can if we want to. All we need to do is keep trying and never give up. This is atleast what I did. 
        </p>
        <p>
			But mind you developing logic isn't easy. It takes a lot of hard work and effort. You need to constantly think of how you can use the resources that the programming language has to offer and implement them in a way to get the desired result. You may fail hundreds of times but in the end you will get it right because each time you commit a mistake you will learn a very important lesson form it, which is – never repeat them again. And by doing this you will know which way you need not think to implement your program. Ultimately you will get what you want out of your code and when you do get it you will feel like you have conquered the world! Slowly but surely things will look a lot simpler and better. Failing should not be a worry but do remember that it should not also be a habit. Keep making mistakes but never make the same mistakes again and again. And always remember – there is always another way to make the same program. It is on you how you look at the problem and find the solution. Do not hesitate to do things differently because sometimes the long shots pay off ! 
		</p>
		<p>
			After a while, I realized that the true essence of logic lied in something that is usually very uncommon today - common sense! And everything seemed simpler. Slowly but surely this realization should dawn upon you and when that day comes then my friends you will be a true programmer. 
        </p>
        <p style="margin-bottom:50px">
        So start off right now and be patient. Also stick to one programming language at a time. Do not try to do too many programming languages at the same time. For beginners I would suggest you start with either Java or C++. These languages form the basis of almost all other languages and once you master any one of these you can shift out to other languages. And by mastering, I am not only talking about the syntax but also the logic. So put on your thinking caps, sit down and code. Be hungry and stay hungry for more and never ever think that you know it all coz frankly no one does... well no one except God!
        </p>
